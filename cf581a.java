import java.util.*;

public class cf581a {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        int b = sc.nextInt();

        if (a > b) {
            a -= b;
            a -= a%2;
            a /= 2;
            System.out.println(b + " " + a);
        } else {
            b -= a;
            b -= b%2;
            b /= 2;
            System.out.println(a + " " + b);
        }


    }
}
